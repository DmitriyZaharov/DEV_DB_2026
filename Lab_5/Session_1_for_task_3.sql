--Сессия 1 «зависнет» (перейдет в состояние ожидания). Она будет ждать завершения транзакции в Сессии 2, 
--так как обе пытаются заблокировать одну и ту же строку (ProductId 3).

--5. Результат в Сессии 1 (после COMMIT во второй сессии)
--a. Каково состояние вашей транзакции?
--Как только Сессия 2 сделает COMMIT, Сессия 1 мгновенно выдаст ошибку:
--ERROR: could not serialize access due to concurrent update
--Почему это произошло?
--На уровне REPEATABLE READ PostgreSQL запрещает изменять строку, которая была изменена и 
