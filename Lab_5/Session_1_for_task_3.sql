SELECT xmin, xmax, "ProductId", "ProductName", "Price" 
FROM public."Goods";

--xmin   |xmax|ProductId|ProductName|Price    |
---------+----+---------+-----------+---------+
--5044106|0   |        1|Велосипед  |$7,550.00|
--5044106|0   |        2|Перчатки   |  $230.00|
--5044106|0   |        3|Насос      |  $150.00|
--5044107|0   |        4|Шлем       |$1,200.00|
--5044110|0   |        5|Покрышка   |  $450.00|

--	4b. Каков результат?

--	Видим 5 записей. К списку добавится «Покрышка».
--	Почему так происходит?
--	В уровне изоляции Read Committed каждый новый запрос SELECT внутри одной транзакции 
--	видит актуальный «снимок» (snapshot) данных на момент начала этого конкретного запроса.
--	Так как Сессия 2 уже зафиксировала (COMMIT) свои изменения, наш второй запрос в Сессии 1 
--	видит эти данные. Это явление называется «Неповторяющееся чтение» (Non-repeatable read) 
--	— когда один и тот же запрос внутри транзакции выдает разные результаты.

-- 4c. Закройте транзакцию
COMMIT; -- Закрываем сессию (или ROLLBACK, в данном случае результат не изменится)


-- =============================================================================
