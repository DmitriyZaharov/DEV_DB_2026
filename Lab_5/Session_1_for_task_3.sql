BEGIN;
SELECT txid_current(); -- Зафиксируйте ID (например, 1001)
SELECT * FROM public."Goods"; -- Видите 3 товара (Велосипед, Перчатки, Насос, Шлем)

SELECT * FROM public."Goods";
--Видим 5 записи. К списку добавится «Фонарь».
--Почему так происходит?
--В уровне изоляции Read Committed каждый новый запрос SELECT внутри одной транзакции 
--видит актуальный «снимок» (snapshot) данных на момент начала этого конкретного запроса.
--Так как Сессия 2 уже зафиксировала (COMMIT) свои изменения, ваш второй запрос в Сессии 1 
--видит эти данные. Это явление называется «Неповторяющееся чтение» (Non-repeatable read) 
--— когда один и тот же запрос внутри транзакции выдает разные результаты.

COMMIT; -- или ROLLBACK, в данном случае результат не изменится

-- Задача 3
-- Этот эксперимент демонстрирует уровень изоляции REPEATABLE READ, при котором транзакция видит 
-- «снимок» данных на момент своего начала и не замечает изменений от других параллельных транзакций.
-- a. Открываем транзакцию REPEATABLE READ
BEGIN ISOLATION LEVEL REPEATABLE READ;

-- b. Извлекаем записи с ProductId > 2 (например, там только 'Насос' с ID=3)
SELECT * FROM public."Goods" WHERE "ProductId" > 2;

-- c. Добавляем новый товар в первой сессии (например, ID=5)
INSERT INTO public."Goods" ("ProductName", "Price") VALUES ('Звонок', 300);

-- a. Снова извлекаем записи ProductId > 2
SELECT * FROM public."Goods" WHERE "ProductId" > 2;

--Мы видим 2 записи: старый «Насос» и новый «Звонок». Мы НЕ увидите «Флягу» из второй сессии, так как:
--Вторая сессия еще не сделала COMMIT.
--Уровень Repeatable Read гарантирует, что вы не увидите новые данные, появившиеся после начала вашей транзакции
--(защита от фантомного чтения в реализации PostgreSQL).
rollback;


